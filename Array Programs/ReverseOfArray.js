var numArray = [10, 20, 30, 40, 50];

console.log("Array Original Sequence: ");
for (var i = 0; i < numArray.length; i++) {
  console.log(numArray[i]);
}

console.log("\n");

console.log("Array in Reverse Order Sequence: ");
for (var i = numArray.length - 1; i >= 0; i--) {
  console.log(numArray[i]);
}
